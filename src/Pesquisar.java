    public void preencherEquipamentos() {
        conecta.conectar();
        conecta.executaSQL("select * from equipamentos order by nome");
        try {
            conecta.rs.first();
            do {
                nometomba.addItem(conecta.rs.getString("nome"));
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro no combobox \n ERRO: " + ex);
        }
    }
    
