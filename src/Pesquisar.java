    private void pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesquisarActionPerformed
       conecta.executaSQL("select * from equipamentos where nome = '"+nometomba.getSelectedItem()+"' order by nome");
        try {
           conecta.rs.first();
            nome.setText(conecta.rs.getString("nome"));
            tombamento.setText(conecta.rs.getString("tombamento"));
            marca.setText(conecta.rs.getString("marca"));
            modelo.setText(conecta.rs.getString("modelo"));
            especificacao.setText(conecta.rs.getString("especificacao"));
            descricao.setText(conecta.rs.getString("descricao"));
            ID.setText(Integer.toString(conecta.rs.getInt("ID")));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao mostrar dados! \n ERRO: " + ex);
        }
        
    }//GEN-LAST:event_pesquisarActionPerformed

    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
       conecta.executaSQL("select * from equipamentos where id = '"+ID.getText()+"'");
        try {
            PreparedStatement pst = conecta.con.prepareStatement("UPDATE equipamentos set Nome = ?, Tombamento = ?, Marca = ?, Modelo = ?, Especificacao = ?, Descricao = ? where ID = ?");
            pst.setString(1, nome.getText());
            pst.setString(2, tombamento.getText());
            pst.setString(3, marca.getText());
            pst.setString(4, modelo.getText());
            pst.setString(5, especificacao.getText());
            pst.setString(6, descricao.getText());
            pst.setInt(7, Integer.parseInt(ID.getText()));
            pst.execute();
            JOptionPane.showMessageDialog(rootPane, "Equipamento Editado com Sucesso!");

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao Alterar \n ERRO: !" + ex);
        }
        nometomba.removeAllItems(); 
        preencherEquipamentos();
    }//GEN-LAST:event_editarActionPerformed

    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
        conecta.executaSQL("select * from equipamentos where id = '"+ID.getText()+"'");
        try {
            PreparedStatement pst = conecta.con.prepareStatement("DELETE FROM equipamentos WHERE id = ?");
            pst.setString(1, ID.getText());
            pst.execute();
            JOptionPane.showMessageDialog(rootPane, "Equipamento Excluido com Sucesso!");

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro na Exclusão \n ERRO: !" + ex);
        }
        nometomba.removeAllItems();
        preencherEquipamentos();
    }//GEN-LAST:event_excluirActionPerformed

